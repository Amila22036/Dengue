import { Component, OnInit } from '@angular/core';
import { UserService } from '../core/user.service';
import { AuthService } from '../core/auth.service';
import { ActivatedRoute } from '@angular/router';
import { Location } from '@angular/common';
import { FormBuilder, FormGroup, Validators } from '@angular/forms';
import { FirebaseUserModel } from '../core/user.model';
import { MainUIService} from '../services/main-ui.service';
import { FullPanelComponentEnum } from '../shared/enums/mainUI.components.enums'

@Component({
  selector: 'page-user',
  templateUrl: 'user.component.html',
  styleUrls: ['user.scss']
})
export class UserComponent implements OnInit{

  user: FirebaseUserModel = new FirebaseUserModel();
  profileForm: FormGroup;
  private FullPanelComponentEnum;

  constructor(
    public userService: UserService,
    public authService: AuthService,
    private route: ActivatedRoute,
    private location : Location,
    private fb: FormBuilder,
    public mainUIService: MainUIService
  ) {
    this.FullPanelComponentEnum = FullPanelComponentEnum
  }

  ngOnInit(): void {
    this.route.params.subscribe(params =>{
      let id = params['id'];
      let project = atob(id);
      let projectObj = '';
      if(project != undefined)
      {
        sessionStorage.setItem('project',project);
        projectObj = JSON.parse(project);
      }
      console.log("project obj ", projectObj)

    })
    this.route.data.subscribe(routeData => {
      let data = routeData['data'];
      if (data) {
        this.user = data;
        this.createForm(this.user.name);
      }
    })
  }

  createForm(name) {
    this.profileForm = this.fb.group({
      name: [name, Validators.required ]
    });
  }

  save(value){
    this.userService.updateCurrentUser(value)
    .then(res => {
      console.log(res);
    }, err => console.log(err))
  }

  logout(){
    this.authService.doLogout()
    .then((res) => {
      this.location.back();
    }, (error) => {
      console.log("Logout error", error);
    });
  }
}
